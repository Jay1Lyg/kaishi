$(function () {
    $(".weixin").on("mouseover",function () {
        $(".tab1").css("display","block")
    })
    $(".weixin").on("mouseout",function () {
        $(".tab1").css("display","none")
    })
    $(".tab1").on("mouseover",function () {
        $(".tab1").css("display","block")
    })
    $(".tab1").on("mouseout",function () {
        $(".tab1").css("display","none")
    })

})